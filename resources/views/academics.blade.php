                        <h3 class="uk-article-title">CURRICULUM</h3>
                    <img class="img-fit:cover" src="staug/img/academics.jpg" alt="">
                    <p>St. Augustine’s College is no exception to what is currently obtainable in the Secondary School curriculum structure of the Nigerian Educational system. The College runs the Junior Secondary School National Curriculum for now. That is, in line with the revised nine-year Basic Education Curriculum. It offers a wide variety of academic subjects taught by dedicated and professional staff. In the Junior Secondary School (Level 1 to 3), the School emphasizes the basic skills in the following subjects: English Language, Mathematics, Basic Science, Basic Technology, Social Studies, Agricultural Science, French, Christian Religious Studies, Fine Art, Physical and Health Education, Home Economics, Information Technology, Business studies, Hausa Language and Security Education.</p>
                        <h3 class="uk-article-title">ACADEMIC STRUCTURE</h3>
                        <p>The academic structure in St. Augustine’s College Du covers a time frame of six years, consisting of 3 years in the junior Secondary School Category and 3 years in the Senior Secondary Cadre. Each academic class (e.g JSS2, SSS1) is termed an academic unit. The academic year unit evolves from the crop of candidates tested and proven to meet the school’s standards through the annual common entrance and subsequent interviews conducted by the school. Each year, the school gives admission to an average of 100 candidates who on accepting and duly satisfying the prerequisites of admission constitute the new academic year unit(JSS1) for a new academic session.</p>
                        <p>All students are expected to imbibe the School culture right from inception (i.e JSS1)
                            In other words, all students are excepted, in the course of the 6years stay in the
                            college, to build a team spirit or better still a “class ethos” which serves as a
                            community within a community. This implies that, an integral concentric layer then
                            enriches the wider circle of academic fellowship with other students and members of staff.</p>
                    <p>The six respective academic year units are expected to meet in parliamentary style every Friday with their form masters/mistresses and coordinating year heads. This gathering is intended to help them collectively and proactively rub minds on that which pertains their welfare and wellbeing within the confines of the college.</p>
                    <p>Each academic year unit is further divided into three streams or categories designated to be in residence in a particular classroom. These three streams are christened Caritas, Unitas and Veritas with the respective connotations.</p>
                    <p><span class="uk-text-large uk-text-bold">CARITAS</span> - depicts genuine concern and love towards
                        others
                        regardless of age
                        and background;
                        The greatest commandment of Christ is to love God and to love neighbour.  The school community and relationships within it are a special place where this love finds expression.</p>
                    <p>In the words of Paul, we are one body.  With the Church we attempt to renew the experience of the early Christian community where “the community of believers was of one heart and mind, and no one claimed that any of his possessions was his own, but they had everything in common.”  (Acts 5:32)</p>
                    <p>The relationships within the community are nourished by the oneness of Christ with each of us and His presence in the community. Within the college, Unitas is expressed in the following:</p>
                    <ul class="uk-list uk-list-bullet">
                        <li>Being a friend.</li>
                        <li>Loving deeds.</li>
                        <li>Service to the community.</li>
                        <li>Care for the common good.</li>
                        <li>Sharing of goods (information, resources talents).</li>
                        <li>Personal interest and support of one another.</li>
                        <li>Bearing shared burdens and responsibilities.</li>
                    </ul>
                    <p><span class="uk-text-large uk-text-bold">UNITAS</span> – This is depicted more succinctly by the expression “one mind, one heart”. It is a deep expression and desire for team work, cooperation and mutual respect. The Christian understanding of being one with one another is based on our common origin in creation by God and re-creation in Christ.</p>
                    <p>In the words of Paul, we are one body.  With the Church we attempt to renew the experience of the early Christian community where “the community of believers was of one heart and mind, and no one claimed that any of his possessions was his own, but they had everything in common.”  (Acts 5:32)</p>
                    <p>The relationships within the community are nourished by the oneness of Christ with each of us and His presence in the community. Within the college, Unitas is expressed in the following:</p>
                    <ul class="uk-list uk-list-bullet">
                        <li>Eagerness to serve the common good.</li>
                        <li>Respect for one another, our diversity, and our talents.</li>
                        <li>Shared identity and membership.</li>
                        <li>Welcoming and lasting friendships.</li>
                        <li>Expressions of unity in community gatherings (e.g., Mass, assemblies, rallies).</li>
                        <li>Realization of the universality of the Church and awareness of being members of a global community.</li>
                        <li>Shared common experiences in sport, studies, and service.</li>
                    </ul>
                    <p><span class="uk-text-large uk-text-bold">VERITAS</span> - depicts a nurturing of a deep rooted desire for fairness and justice. Sincerity of conduct and integrity of words and intention are key in this regard. VERITAS (Truth)  - The search for God through the pursuit of Truth</p>
                    <p>We are aware that God is the source of all truth.  With faith and reason, like two great wings, we fly to Truth.  Augustine insists that we are to “teach the truth in love.”Veritas is expressed in the following:</p>
                    <ul class="uk-list uk-list-bullet">
                        <li>Telling the truth</li>
                        <li>Searching for truth in relationships.</li>
                        <li>Being serious about the hard work, self-discipline, and self-awareness that honest study requires.</li>
                        <li>Knowing Him who is Truth Incarnate, Jesus Christ and His saving message.</li>
                        <li>Knowing one’s worth as a child of God and the transcendent dignity of every human person.</li>
                        <li>Exploring energetically the physical world and the world of ideas with an openness to the goodness and beauty of reality.</li>
                        <li>Being able to think in an orderly and critical fashion and to express one’s thoughts clearly and with grace.</li>
                    </ul>
                    <p>The three Latin expressions aforementioned are reflective of the Augustinian Charism of charity, unity and truth. By designating the streams in such manner, it is expected that the students are spiritually and socially nurtured along this moral lines since Education without character amounts to nothing.</p>
                    <h3 class="uk-article-title">STUDENT ACADEMIC PROBATION</h3>
                    <p>In view of the need to heighten academic performance of students in general and to equally stimulate their zest for core subjects such as Mathematics and English Language, the management of the school has introduced a sit back order/ resit policy. This simply implies that all students must have a CGPA of 4.0 to be promoted to the next class. Failing a subject automatically places a student in probation. This probational module is also specifically designed to identify areas where students demonstrate underperformance or academic weakness.</p>
                    <p>Having identified such areas of weakness, close attention is paid to these crop of students to ensure they build a sense of confidence in themselves and towards the subject question. </p>
                    <p>Suffice it to equally say that the student only exit the probationary module only after having demonstrated marked improvement in his/her area of academic weakness. </p>
